interface Locatable {

    /**
     * Returns the X position
     * 
     * @return int
     */
    public int getX();

    /**
     * Returns the Y position
     * 
     * @return
     */
    public int getY();

    /**
     * Sets the X and Y pos for the Locatable
     */
    public void setPos(int x, int y);

}
